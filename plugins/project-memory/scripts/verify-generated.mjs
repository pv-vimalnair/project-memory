#!/usr/bin/env node
// @ts-check
import { Buffer } from "node:buffer";
import { mkdir, readFile, readdir, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath, pathToFileURL, URL } from "node:url";

import { importCatalogGoldenCases } from "../dist/benchmark/run-benchmark.js";
import { loadCatalog } from "../dist/catalog/load-catalog.js";
import { buildCatalogRelease } from "../dist/catalog/manifest/build-catalog-bundle.js";
import { canonicalJson } from "../dist/core/canonical-json.js";
import { emitGeneratedYaml } from "../dist/core/document-io.js";
import { emitJsonSchemas } from "../dist/schema/emit.js";
import { PROJECT_SCHEMA_REGISTRARS } from "../dist/schema/project-registrars.js";
import { registerProjectSchemas } from "../dist/schema/registry.js";

const packageRoot = fileURLToPath(new URL("../", import.meta.url));
const temporaryRoot = path.join(packageRoot, ".tmp", "verify-generated");

/** @param {string} root */
async function fileInventory(root) {
  /** @type {string[]} */
  const files = [];
  const pending = [root];
  while (pending.length > 0) {
    const current = pending.pop();
    if (current === undefined) continue;
    for (const entry of await readdir(current, { withFileTypes: true })) {
      const target = path.join(current, entry.name);
      if (entry.isSymbolicLink()) throw new Error(`generated tree contains symlink: ${target}`);
      if (entry.isDirectory()) pending.push(target);
      if (entry.isFile() && entry.name !== ".gitkeep") {
        files.push(path.relative(root, target).replaceAll(path.sep, "/"));
      }
    }
  }
  return files.sort((left, right) => Buffer.compare(Buffer.from(left), Buffer.from(right)));
}

/** @param {string} expectedRoot @param {string} actualRoot */
async function compareTrees(expectedRoot, actualRoot) {
  const expectedFiles = await fileInventory(expectedRoot);
  const actualFiles = await fileInventory(actualRoot);
  if (JSON.stringify(actualFiles) !== JSON.stringify(expectedFiles)) {
    throw new Error(`generated file inventory drift: ${expectedRoot}`);
  }
  for (const relative of expectedFiles) {
    const [expected, actual] = await Promise.all([
      readFile(path.join(expectedRoot, relative)),
      readFile(path.join(actualRoot, relative)),
    ]);
    if (!expected.equals(actual)) throw new Error(`generated bytes drift: ${relative}`);
  }
  return expectedFiles.length;
}

await rm(temporaryRoot, { recursive: true, force: true });
await mkdir(temporaryRoot, { recursive: true });
const registered = registerProjectSchemas(PROJECT_SCHEMA_REGISTRARS);
if (!registered.ok) throw new Error(JSON.stringify(registered.issues));

const schemaOutput = path.join(temporaryRoot, "schemas");
const schemas = await emitJsonSchemas(pathToFileURL(`${schemaOutput}${path.sep}`));
if (!schemas.ok) throw new Error(JSON.stringify(schemas.issues));
const schemaCount = await compareTrees(
  path.join(packageRoot, "schemas", "project-memory", "v1"),
  schemaOutput,
);

const catalogRoot = new URL("../catalog/project-memory/v1/", import.meta.url);
const catalog = await loadCatalog(catalogRoot);
if (!catalog.ok) throw new Error(JSON.stringify(catalog.issues));
const benchmark = emitGeneratedYaml({
  schema_version: "1.0.0",
  cases: importCatalogGoldenCases(catalog.value),
});
if (!benchmark.ok) throw new Error(JSON.stringify(benchmark.issues));
const benchmarkOutput = path.join(temporaryRoot, "catalog-golden-cases.yaml");
await writeFile(benchmarkOutput, benchmark.value, "utf8");
const trackedBenchmark = await readFile(
  path.join(packageRoot, "benchmarks", "briefs", "catalog-golden-cases.yaml"),
);
if (!trackedBenchmark.equals(await readFile(benchmarkOutput))) {
  throw new Error("generated benchmark brief bytes drift");
}

const releaseA = path.join(temporaryRoot, "release-a");
const releaseB = path.join(temporaryRoot, "release-b");
for (const outputRoot of [releaseA, releaseB]) {
  await mkdir(outputRoot, { recursive: true });
  const built = await buildCatalogRelease({
    sourceRoot: catalogRoot,
    outputRoot: pathToFileURL(`${outputRoot}${path.sep}`),
    release: "1.0.0",
  });
  if (!built.ok) throw new Error(JSON.stringify(built.issues));
}
const releaseRelative = path.join("dist", "catalog", "project-memory", "1.0.0");
const catalogArtifactCount = await compareTrees(
  path.join(releaseA, releaseRelative),
  path.join(releaseB, releaseRelative),
);

process.stdout.write(canonicalJson({
  schema_version: "1.0.0",
  valid: true,
  tracked_schema_files: schemaCount,
  tracked_benchmark_cases: 150,
  deterministic_catalog_artifacts: catalogArtifactCount,
}));
